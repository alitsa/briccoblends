<h2>Copy clips</h2>
<small>Updated 12/19/2021</small>
<p>Here are some commonly asked questions if you're looking for quick and easy content to copy/paste into your article. These clips take the form of an interview with Bricco Blends founder, <strong>Amalia Litsa</strong></p>

<h4>Where did the idea for Bricco Blends come from?</h4>
<p>
  Bricco Blends is a solution that came out of the pandemic. I co-own a coffee shop in Austin, TX with my friend, Joshua Adrian. It took us about 9 months to form the business, get loans, and build out the space. The pandemic struck Austin and the city completely shut down right when we finally got our certificate of occupancy. As a new business, we had zero help: no federal, state, or city government relief of any kind. We had to earn customers in that environment. It was very scary, to say the least!
</p>
<p>
  As a vegan shop, the cost of milk for us was easily 4x that of cow milk. I thought maybe I could improve our margins by making oat milk in house. Oats are cheap, right? But after many nights of pureeing and straining, I realized the labor cost would be prohibitive.
</p>
<p>
  I wondered if there was any way my baristas could skip all the hard parts. Oats are dry&mdash;could the oat milk start off as a powder?
</p>
<p>
  It took me about a year of experimentation and testing to arrive at a formula I really liked. I'm quite proud of its flavor and how it performs under a steam wand.
</p>
<h4>Do you have a food science background?</h4>
<p>
  No, but I am a product designer. I left tech to create experiences and design products in the real world. I love being connected to my customers and using my problem solving skills to help people. It's so gratifying.
</p>
<h4>What makes Bricco Blends different?</h4>
<p>
  There are a few powdered oat milks out there, but nothing for coffee shops. Bricco Blends <em>wants</em> to foam. Gorgeous microfoam comes down to the balance of oils, proteins, and minerals in the oat milk. It took hundreds of cappuccinos for me to arrive at something that everyone liked, but I think we nailed it. I'm forever grateful to the baristas that helped me with their candid feedback and my alpha customers (Barrett's Coffee and Be Kind Coffee) for incorporating Bricco Blends into their operations.
</p>
<h4>What does "Bricco" mean?</h4>
<p>
Bricco means little jug. A "Bricco per latte" is a milk jug, or frothing pitcher as baristas might call it.
</p>
<h4>Why did you make Bricco Blends eco-friendly?</h4>
<p>
  My own coffee shop goes through about 60 gallons of oat milk every week. That's 240 Tetra Pak cartons in the landfill <em>every week</em>! I talked to other coffee shop owners in town, and overall, it seems shops here average at around 30 gallons (120 Tetra Paks) per week. I happen to know our distributor has a clientbase of around 600, so we're throwing over 3 million of those cartons in our landfill every week, easily.
</p>
<p>
  When I landed on the powdered formula solution, I realized Bricco Blends could have a meaningful environmental impact in a couple ways. First, I can use compostable packaging because my product is dry. Second, Bricco Blends takes up 80% less space than cartons. This means I can significantly reduce shipping by loading more oat milk on a pallet.
</p>
<p>
  A lot of people switch to oat milk for environmental reasons because it takes far fewer natural resources to produce than cow milk, and its production generates less pollution too. It almost doesn't matter, though, because it requires so much packaging and shipping. I'm so pleased to offer a solution that is truly environmentally friendly from end to end.
</p>
<h4>What are your plans for the future?</h4>
<p>
  Oh man, my whole roadmap is clear as day. I can't wait to tackle the next thing, but I'm exercising discipline and focusing on scaling up production on my initial offering right now: Bricco Blends Barista Oat Milk Powder.
</p>
<h4>Are you worried about competitors?</h4>
<p>
  There are already powdered oat milks out there, but to my knowledge, they are all formulated around consumer use. I'd love to see Bricco Blends become a house name in the coffee industry. My design is guided by real equipment, real baristas, and real coffee shop customers. As Bricco Blends grows, increased capital will allow me to pursue increasingly elegant solutions, but when it comes to understanding real problems, I already have a wealth of resources. I feel so forunate to have this opportunity to create something so authentic.
</p>
<h4>Do you plan to make CPG products too?</h4>
<p>
  I'd love to see neighborhood coffee shops become destinations for fresh, locally made oat milk. For that reason, my primary focus is B2B, not grocery. I will have some CPG, but I'd like to shape a product portfolio for Bricco Blends that ultimately supports small food business.
</p>
<h4>Where is your coffee shop?</h4>
<p>
I co-own Dear Diary Coffeehouse in Austin, TX with my friend, Joshua Adrian. It's located at 1212 Chicon St #103.
</p>
<p>
Dear Diary Coffeehouse is a little vegan coffee shop in East Austin. You can learn more about it at <a target="_blank" href="https://deardiarycoffeehouse.com">deardiarycoffeehouse.com</a>
</p>
<h4>How has veganism influenced you?</h4>
<p>
My shop only uses plant milk because I personally would feel so guilty about all the cow milk I would otherwise buy. Coffee shops are really milk shops, when it comes down to it. Our most popular product is the latte, and that's 80% milk! I just think of the mother and baby cows and can't do it.
</p>
<p>
However! Veganism is really tough on coffee shop margins. Our milk is $10 – $12 / gallon, whereas omnivore shops are only paying $3 – $5 / gallon. We also want to take care of our baristas and pay good, living wages, so we have to work extra hard to gain back that revenue from somewhere.
</p>
<p>
One day, I think it would be cool to assist vegan coffee shops with milk costs, just because I know how hard it is. This is something I'd like to do when Bricco Blends scales up and our own production costs come down.
</p>
<p>
At the end of the day, though, I want to serve and support all small food business owners; I care a lot about nuturing the local economy. I know everyone has their own paths, and I think it's much more productive to honor and celebrate folks' unique contributions to society and the planet. We are all simultaneously constructive and destructive, so thank goodness we're all different as well!
</p>

